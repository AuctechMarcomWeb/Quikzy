<script>
document.addEventListener('DOMContentLoaded', function() {

    var modalElement = document.getElementById('preBookingModal');
    if (!modalElement) return;

    var myModal = new bootstrap.Modal(modalElement);

    /* ===== DETECT INDEX PAGE (index , index/ , index.php , / ) ===== */
    var path = window.location.pathname.toLowerCase();

    var isHomePage =
        path === "/" ||
        path.endsWith("/index") ||
        path.endsWith("/index/") ||
        path.endsWith("/index.php");

    /* ===== AUTO OPEN ONLY IF NOT SUBMITTED ===== */
    if (isHomePage && !localStorage.getItem("cabBookingSubmitted")) {

        window.addEventListener("load", function() {
            setTimeout(function() {
                myModal.show();
            }, 400);
        });
    }

    /* ===== BUTTON CLICK OPEN (ALL PAGES) ===== */
    document.querySelectorAll('.tx26-open-modal').forEach(function(btn) {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            myModal.show();
        });
    });

});
</script>

<script>
$(document).ready(function() {

    $("#cabBookingForm").on("submit", function(e) {
        e.preventDefault();

        let form = $(this);
        let formData = form.serialize();
        let submitBtn = $("#submitBookingBtn");

        /* Disable button */
        submitBtn.prop("disabled", true).text("Processing...");

        /* Show fullscreen loader */
        $("#fullScreenLoader").fadeIn(200);

        $.ajax({
            url: "booking_insert.php",
            type: "POST",
            data: formData,
            dataType: "json",

            success: function(response) {

                $("#fullScreenLoader").fadeOut(200);
                submitBtn.prop("disabled", false).text("Confirm Booking");

                if (response.status === "success") {

                    // save submission state
                    localStorage.setItem("cabBookingSubmitted", "yes");

                    Swal.fire({
                        icon: "success",
                        title: "Thank You!",
                        text: "Your booking request has been submitted successfully. Our team will contact you soon to confirm your ride.",
                        confirmButtonColor: "#fd9206"
                    });

                    form.trigger("reset");
                } else {
                    Swal.fire({
                        icon: "error",
                        title: "Error",
                        text: response.message
                    });
                }
            },

            error: function() {

                $("#fullScreenLoader").fadeOut(200);
                submitBtn.prop("disabled", false).text("Confirm Booking");

                Swal.fire({
                    icon: "error",
                    title: "Server Error",
                    text: "Something went wrong. Please try again."
                });
            }
        });

    });

});
</script>

